package o20170301;

public class In5MathTest {
	
	public static void main(String[] args) {
		System.out.println("E=" +Math.E); // 自然对数
		System.out.println("E=" +Math.PI); // 圆周率
		System.out.println("pow(2, 32)=" +Math.pow(2, 32)); // 幂函数
		System.out.println("abs(-111.1)=" +Math.abs(-111.1));
		System.out.println("log10(10)=" +Math.log10(10)); //对数函数
		System.out.println("max(10, 5)=" +Math.max(10, 5)); // 取两者间最大值
		System.out.println("sqrt(81)=" +Math.sqrt(81)); // 平方根
		System.out.println("signum(-2)=" +Math.signum(-2)); // 符号函数

	}
	
}
