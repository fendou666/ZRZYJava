package basic_20170209;

public class A1PersonalInfo {
	
	String name;
	String sex;
	int age;
	String address;
	String birthday;
	
	void setName(String scanName) {
		name = scanName;
	};
	String getName() {
		return name;
	};
	void setSex(String scanSex) {
		sex = scanSex;
	};
	String getSex() {
		return sex;
	};
	void setAge(int scanAge) {
		age = scanAge;
	};
	int getAge() {
		return age;
	};
	void setAddress(String scanAddress) {
		address = scanAddress;
	};
	String getAddress() {
		return address;
	};
	void setBirthday(String scanBirthday) {
		birthday = scanBirthday;
	};
	String getBirthday() {
		return birthday;
	};
	
	
	
}
